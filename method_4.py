# ── 3. COINCIDENCE PARSER
def parse_coinc(raw_bytes):
    text = raw_bytes.decode('utf-8', errors='replace')
    result = {}
    for line in text.splitlines():
        line = line.strip()
        if not line or line.startswith('#'):
            continue
        cols = line.split('\t')
        if len(cols) < 6:
            continue
        try:
            label = cols[0].strip().upper() + cols[1].strip().upper()
            result[label] = np.array([float(cols[2]), float(cols[3]),
                                      float(cols[4]), float(cols[5])])
        except (ValueError, IndexError):
            pass
    return result

# ── 4. PROJECTORS
sq2 = 1.0 / np.sqrt(2)
STATES = {
    'H': np.array([1,       0      ], dtype=complex),
    'V': np.array([0,       1      ], dtype=complex),
    'D': np.array([sq2,     sq2    ], dtype=complex),
    'A': np.array([sq2,    -sq2    ], dtype=complex),
    'R': np.array([sq2,  sq2*1j   ], dtype=complex),
    'L': np.array([sq2, -sq2*1j   ], dtype=complex),
}
BASIS_EIGENSTATES = {
    'H': ('H', 'V'),
    'D': ('D', 'A'),
    'R': ('R', 'L'),
}

def projector(s):
    v = STATES[s]
    return np.outer(v, v.conj())

def get_projectors(label):
    sp, sm = BASIS_EIGENSTATES[label[0]]
    ip, im = BASIS_EIGENSTATES[label[1]]
    return [
        np.kron(projector(sp), projector(ip)),
        np.kron(projector(sp), projector(im)),
        np.kron(projector(sm), projector(ip)),
        np.kron(projector(sm), projector(im)),
    ]

ALL_PROJECTORS = {
    lbl: get_projectors(lbl)
    for lbl in ['HH', 'HD', 'HR', 'DH', 'DD', 'DR', 'RH', 'RD', 'RR']
}

# ── 5. MLE DENSITY MATRIX — Cholesky parametrization
def params_to_rho(params):
    T = np.zeros((4, 4), dtype=complex)
    idx = 0
    for i in range(4):
        T[i, i] = params[idx]
        idx += 1
    for i in range(1, 4):
        for j in range(i):
            T[i, j] = params[idx] + 1j * params[idx + 1]
            idx += 2
    rho = T.conj().T @ T
    tr = np.trace(rho)
    if abs(tr) < 1e-12:
        return np.eye(4, dtype=complex) / 4.0
    return rho / tr

def neg_log_likelihood(params, coinc_dict):
    rho = params_to_rho(params)
    nll = 0.0
    for label, projs in ALL_PROJECTORS.items():
        if label not in coinc_dict:
            continue
        counts = coinc_dict[label]
        if counts.sum() < 1:
            continue
        for k in range(4):
            p_k = max(1e-10, float(np.real(np.trace(rho @ projs[k]))))
            if counts[k] > 0:
                nll -= counts[k] * np.log(p_k)
    return nll

def is_physical(rho, atol_tr=1e-3, atol_eig=1e-4):
    tr   = float(np.real(np.trace(rho)))
    eigs = np.linalg.eigvalsh(rho)
    return np.isclose(tr, 1.0, atol=atol_tr) and np.all(eigs >= -atol_eig)

def mle_reconstruct(coinc_dict):
    available = [k for k in ALL_PROJECTORS
                 if k in coinc_dict and coinc_dict[k].sum() > 0]
    if len(available) < 9:
        return None

    def run_opt(x0):
        return minimize(
            neg_log_likelihood, x0,
            args=(coinc_dict,),
            method='L-BFGS-B',
            options={'maxiter': 5000, 'ftol': 1e-12, 'gtol': 1e-8}
        )

    x0 = np.zeros(16)
    x0[0] = x0[4] = x0[9] = x0[15] = 0.5
    res = run_opt(x0)
    rho = params_to_rho(res.x)
    if res.success and is_physical(rho):
        return rho

    for attempt in range(3):
        rng = np.random.default_rng(attempt * 7 + 13)
        x0r = rng.normal(0, 0.3, 16)
        for di in [0, 4, 9, 15]:
            x0r[di] = abs(x0r[di]) + 0.1
        res2 = run_opt(x0r)
        rho2 = params_to_rho(res2.x)
        if is_physical(rho2):
            print(f"    INFO: MLE converged on retry {attempt + 1}")
            return rho2

    print("    ERROR: MLE failed all retries — sample SKIPPED")
    return None

# ── 6. PURITY
def purity(rho):
    return float(np.real(np.trace(rho @ rho)))

# ── 7. CONCURRENCE — Wootters (1998) PRL 80, 2245
sy   = np.array([[0, -1j], [1j, 0]], dtype=complex)
SYSY = np.kron(sy, sy)

def concurrence(rho):
    rho_tilde  = SYSY @ rho.conj() @ SYSY
    eigs, vecs = np.linalg.eigh(rho)
    sqrt_rho   = vecs @ np.diag(np.sqrt(np.maximum(eigs, 0))) @ vecs.conj().T
    R          = sqrt_rho @ rho_tilde @ sqrt_rho
    R          = (R + R.conj().T) / 2.0
    R_eigs     = np.linalg.eigvalsh(R)
    lambdas    = np.sort(np.sqrt(np.maximum(R_eigs, 0)))[::-1]
    return max(0.0, float(np.real(lambdas[0] - lambdas[1] - lambdas[2] - lambdas[3])))

# ── 8. BOOTSTRAP — multinomial resampling (matches Method 3)
def bootstrap_concurrence(coinc_dict, n_boot=200):
    C_boot   = []
    n_failed = 0
    for _ in range(n_boot):
        resampled = {}
        for label, counts in coinc_dict.items():
            N_float = counts.sum()
            if N_float < 1:
                continue
            p = counts / N_float
            resampled[label] = np.random.multinomial(
                int(round(N_float)), p
            ).astype(float)
        rho_b = mle_reconstruct(resampled)
        if rho_b is None:
            n_failed += 1
            continue
        C_b = concurrence(rho_b)
        if not np.isnan(C_b):
            C_boot.append(C_b)
    if n_failed > 0:
        pct = 100.0 * n_failed / n_boot
        msg = "WARNING" if pct > 10 else "INFO"
        print("    " + msg + ": " + str(n_failed) + "/" + str(n_boot) +
              " (" + f"{pct:.0f}" + "%) bootstrap resamples failed MLE")
    if len(C_boot) < 10:
        print("    WARNING: only " + str(len(C_boot)) + " valid samples — result unreliable")
        return np.nan, np.nan
    return float(np.mean(C_boot)), float(np.std(C_boot))

