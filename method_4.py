# ── 2. CALIBRATION: vpos -> real dt (picoseconds)
def load_vpos_to_dt(cache, fiber_key):
    key = next((k for k in cache
                if 'delays_and_vpos_dict' in k.lower()
                and fiber_key.lower() in k.lower()
                and '__macosx' not in k.lower()), None)
    if key is None:
        print(f"WARNING: calibration file not found for {fiber_key}")
        return {}
    text = cache[key].decode('utf-8', errors='replace')
    mapping = {}
    for line in text.splitlines():
        line = line.strip()
        if not line or line.startswith('#') or line.startswith('channel'):
            continue
        parts = line.split()
        try:
            mapping[int(parts[0].replace('vpos_', ''))] = float(parts[-1])
        except (ValueError, IndexError):
            pass
    return mapping

