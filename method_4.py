def run_fiber(paths, fiber_label, vpos_to_dt):
    results = []
    skipped = 0
    print(f"\n{fiber_label}:")
    for path in paths:
        m = re.search(r'vpos_(\d+)', path)
        if not m:
            skipped += 1
            continue
        vpos = int(m.group(1))
        dt   = vpos_to_dt.get(vpos, None)
        if dt is None:
            skipped += 1
            continue

        coinc = parse_coinc(file_cache[path])
        rho   = mle_reconstruct(coinc)
        if rho is None:
            skipped += 1
            continue

        pur           = purity(rho)
        C_pt          = concurrence(rho)
        C_mean, C_std = bootstrap_concurrence(coinc, n_boot=200)

        if np.isnan(C_mean):
            skipped += 1
            continue

        entangled = (C_mean - 2.0 * C_std) > 0.0
        pur_str   = ('pure' if pur > 0.95 else 'mixed' if pur < 0.50 else 'part.mixed')
        status    = 'ENTANGLED' if entangled else 'not confirmed'

        results.append({
            'vpos': vpos, 'dt_ps': dt,
            'C_point': C_pt, 'C_mean': C_mean, 'C_std': C_std,
            'purity': pur, 'entangled': entangled
        })
        print(f"  dt={dt:>7.2f}ps  "
              f"C={C_mean:.4f}+/-{C_std:.4f}  "
              f"pur={pur:.3f}({pur_str})  "
              f"{status}")

    if skipped:
        print(f"  [{skipped} points skipped]")
    results.sort(key=lambda r: r['dt_ps'])
    return results

