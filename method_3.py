def process_chsh(paths, fiber_label, vpos_to_dt, n_boot=200):
    results = []
    skipped = 0
    print(f"\n{fiber_label}:")
    for path in paths:
        m = re.search(r'vpos_(\d+)', path)
        if not m:
            skipped += 1
            continue
        vpos = int(m.group(1))
        dt   = vpos_to_dt.get(vpos, None)
        if dt is None:
            skipped += 1
            continue

        coinc = parse_coinc(file_cache[path])
        rho   = mle_reconstruct(coinc)
        if rho is None:
            skipped += 1
            continue

        pur              = purity(rho)
        S_mle, m1, m2   = horodecki_smax(rho)
        S_mean, S_std    = bootstrap_smax(coinc, n_boot=n_boot)

        if np.isnan(S_mean):
            skipped += 1
            continue

        entangled    = (S_mean - 2.0 * S_std) > S_CLASSICAL
        significance = (S_mean - S_CLASSICAL) / S_std if S_std > 0 else float('inf')

        pur_str = ('pure'       if pur > 0.95
                   else 'mixed' if pur < 0.50
                   else 'part.mixed')
        status  = 'NONLOCAL -> ENTANGLED' if entangled else 'not confirmed'

        results.append({
            'vpos': vpos, 'dt_ps': dt,
            'S_mle': S_mle, 'S_mean': S_mean, 'S_std': S_std,
            'purity': pur, 'entangled': entangled,
            'significance': significance,
            'm1': m1, 'm2': m2
        })

        print(f"  dt={dt:>7.2f}ps  "
              f"S={S_mean:.4f}+/-{S_std:.4f}  "
              f"sig={significance:+.1f}s  "
              f"pur={pur:.3f}({pur_str})  "
              f"{status}")

    if skipped:
        print(f"  [{skipped} points skipped]")
    results.sort(key=lambda r: r['dt_ps'])
    return results

