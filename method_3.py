    text = cache[key].decode('utf-8', errors='replace')
    mapping = {}
    for line in text.splitlines():
        line = line.strip()
        if not line or line.startswith('#') or line.startswith('channel'):
            continue
        parts = line.split()
        try:
            mapping[int(parts[0].replace('vpos_', ''))] = float(parts[-1])
        except (ValueError, IndexError):
            pass
    return mapping

