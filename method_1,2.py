# ── 5. PARSE COINCIDENCE FILES ─────────────────────────────────
def parse_coinc(raw_bytes):
    text = raw_bytes.decode('utf-8', errors='replace')
    result = {}
    for line in text.splitlines():
        line = line.strip()
        if not line or line.startswith('#'):
            continue
        cols = line.split('\t')
        if len(cols) < 6:
            continue
        try:
            label = cols[0].strip().upper() + cols[1].strip().upper()
            result[label] = {
                'hs_hi': float(cols[2]),
                'hs_vi': float(cols[3]),
                'vs_hi': float(cols[4]),
                'vs_vi': float(cols[5]),
            }
        except (ValueError, IndexError):
            pass
    return result

# ── 6. CORRELATOR + BELL FIDELITY + WITNESS ────────────────────
# E(a,b) = (hs_hi + vs_vi - hs_vi - vs_hi) / N
# F(|Ψ⁻⟩) = (1/4)(1 - E_XX - E_YY - E_ZZ)
# W = 0.5 - F  →  W < 0 proves entanglement
def correlator(counts):
    N = counts['hs_hi'] + counts['hs_vi'] + counts['vs_hi'] + counts['vs_vi']
    if N < 1:
        return np.nan
    return (counts['hs_hi'] + counts['vs_vi'] - counts['hs_vi'] - counts['vs_hi']) / N

def bell_fidelity(d):
    for key in ['HH', 'DD', 'RR']:
        if key not in d:
            return np.nan, np.nan, np.nan, np.nan
    E_ZZ = correlator(d['HH'])
    E_XX = correlator(d['DD'])
    E_YY = correlator(d['RR'])
    if any(np.isnan(v) for v in [E_ZZ, E_XX, E_YY]):
        return np.nan, E_ZZ, E_XX, E_YY
    return 0.25 * (1.0 - E_XX - E_YY - E_ZZ), E_ZZ, E_XX, E_YY

# ── 7. MULTINOMIAL BOOTSTRAP ───────────────────────────────────
# n_boot=200: consistent with Methods 3 and 4
def bootstrap_F(d, n_boot=200):
    boot = []
    for _ in range(n_boot):
        rd = {}
        for label, counts in d.items():
            arr = np.array([counts['hs_hi'], counts['hs_vi'],
                            counts['vs_hi'], counts['vs_vi']])
            N = arr.sum()
            if N < 1:
                continue
            sampled = np.random.multinomial(int(N), arr / N).astype(float)
            rd[label] = {
                'hs_hi': sampled[0], 'hs_vi': sampled[1],
                'vs_hi': sampled[2], 'vs_vi': sampled[3]
            }
        F, *_ = bell_fidelity(rd)
        if not np.isnan(F):
            boot.append(F)
    if len(boot) < 10:
        return np.nan, np.nan
    return float(np.mean(boot)), float(np.std(boot))

# ── 8. FIND COINCIDENCE FILES ──────────────────────────────────
def find_all_coinc(cache, fiber_tag):
    out = []
    for p in cache:
        pl = p.lower()
        if (fiber_tag.lower() in pl
                and pl.endswith('coincidences.txt')
                and 'singles' not in pl):
            out.append(p)
    out.sort(key=lambda p: int(re.search(r'vpos_(\d+)', p).group(1))
             if re.search(r'vpos_(\d+)', p) else 0)
    return out

