def process_files(paths, fiber_label, vpos_to_dt):
    results = []
    skipped = 0
    print(f"\n{fiber_label}:")
    for path in paths:
        m = re.search(r'vpos_(\d+)', path)
        if not m:
            skipped += 1
            continue
        vpos = int(m.group(1))
        dt = vpos_to_dt.get(vpos, None)
        if dt is None:
            skipped += 1
            continue
        d = parse_coinc(file_cache[path])
        F, E_ZZ, E_XX, E_YY = bell_fidelity(d)
        if np.isnan(F):
            skipped += 1
            continue

        F_mean, F_std = bootstrap_F(d, n_boot=200)
        W      = 0.5 - F_mean          # witness from bootstrap mean
        W_std  = F_std                  # σ_W = σ_F (0.5 is constant)

        # ── FIX: 2-sigma lower bound criterion (consistent with Methods 3 & 4)
        entangled_2sig = (F_mean - 2.0 * F_std) > 0.5

        results.append({
            'vpos': vpos, 'dt_ps': dt,
            'F': F_mean, 'F_std': F_std,
            'W': W, 'W_std': W_std,
            'entangled_2sig': entangled_2sig,
            'E_ZZ': E_ZZ, 'E_XX': E_XX, 'E_YY': E_YY
        })

        status = 'ENTANGLED (2-sigma)' if entangled_2sig else 'not confirmed'
        print(f"  dt={dt:>7.2f}ps (vpos={vpos:>3})  "
              f"F={F_mean:.4f}+/-{F_std:.4f}  W={W:.4f}  {status}")

    if skipped:
        print(f"  [{skipped} files skipped: not valid coincidence data]")
    results.sort(key=lambda r: r['dt_ps'])
    return results

